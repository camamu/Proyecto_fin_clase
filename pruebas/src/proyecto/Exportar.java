        // TODO add your handling code here:
        Xml exportar = new Xml();
        switch (opcion){
            case 1:
                exportar.exportarClientes();
                try{
                File file1 = new File("C:/Informes/clientes.xml");
                Desktop.getDesktop().open(file1);
                } catch(Exception e) {
                e.printStackTrace();
                }
                break;
            case 2:
                exportar.exportarUsuarios();
                try{
                File file2 = new File("C:/Informes/usuarios.xml");
                Desktop.getDesktop().open(file2);
                } catch(Exception e) {
                e.printStackTrace();
                }
                break;
            default:
                JOptionPane.showMessageDialog(null,"selecciona una opci√≥n");
        }
    
        
